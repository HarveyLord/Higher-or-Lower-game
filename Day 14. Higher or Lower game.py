import random

def main_game():

    # variables

    index_swap = 0
    rounds = 0
    game_over = False
    print("~~~~~ Welcome to the Higher or Lower game ~~~~~\n")

    # main while loop

    while not game_over:

        list_index_1 = 0
        list_index_2 = 0

        if rounds == 0:
            while list_index_1 == list_index_2:
                list_index_1 = random.randint(0, 49)
                list_index_2 = random.randint(0, 49)
                if list_index_1 != list_index_2:
                    index_swap = list_index_2
                    break

        if rounds >= 1:
            list_index_1 = index_swap
            list_index_2 = random.randint(0, 49)
            index_swap = list_index_2


        person_1 = data[list_index_1]
        person_2 = data[list_index_2]

        list_person_1 = []
        list_person_2 = []

        for key, value in person_1.items():
            if key == "name":
                list_person_1.append(person_1[key])
            elif key == "follower_count":
                list_person_1.append(person_1[key])
            elif key == "description":
                list_person_1.append(person_1[key])
            elif key == "country":
                list_person_1.append(person_1[key])

        for key, value in person_2.items():
            if key == "name":
                list_person_2.append(person_2[key])
            elif key == "follower_count":
                list_person_2.append(person_2[key])
            elif key == "description":
                list_person_2.append(person_2[key])
            elif key == "country":
                list_person_2.append(person_2[key])

        ## console stage

        print(f"Compare A: {list_person_1[0]}, a {list_person_1[2]}, from {list_person_1[3]}")
        print(f"Against B: {list_person_2[0]}, a {list_person_2[2]}, from {list_person_2[3]}\n")
        answer = input("Who has more followers? 'A' or 'B': ").upper()


        followers_1 = int(list_person_1[1])
        followers_2 = int(list_person_2[1])
        x = followers_1 - followers_2

        ## logic statements

        if answer == "A":

            if x > 0:
                print(f"Correct answer, {list_person_1[0]} has {followers_1} million followers\n")
                rounds += 1

            elif x < 0:
                print(f"Wrong answer, {list_person_1[0]} has {followers_1} million followers, while {list_person_2[0]} has {followers_2} million followers\n")
                game_over = True

            elif x == 0:
                print(f"Trick question they both have {followers_1} million followers\n")

        if answer == "B":

            if x < 0:
                print(f"Correct answer, {list_person_2[0]} has {followers_2} million followers\n")
                rounds += 1

            elif x > 0:
                print(f"Wrong answer, {list_person_2[0]} has {followers_2} million followers, while {list_person_1[0]} has {followers_1} million followers\n")
                game_over = True

            elif x == 0:
                print(f"Trick question they both have {followers_1} million followers\n")
        else:
            print("You must enter either 'A' or 'B'\n")
            game_over = True


    print(f"Game over, you lasted {rounds} rounds")
    again = input("Would you like to play again? 'Y' or 'N': ").upper()

    if again == "Y":
        main_game()
    else:
        print("Thank you for playing")

main_game()

